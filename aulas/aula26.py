# Formatação basica de strings

variavel = 'ABCD'

print(f'{variavel}')
print(f'{variavel: >10}') #preenche a esquerda com 10 caracter
print(f'{variavel: <10}.') #preenche a direita com 10 caracter
print(f'{variavel: ^10}.') #centraliza
print(f'{1000.45645797:.1f}')
print(f'O hexadecimal de 1500 é {1500:08X}')